/* ---------------- i18n helpers ---------------- */
function t(key){ var L = I18N[cfg.lang] || I18N.en; return (L[key] !== undefined) ? L[key] : (I18N.en[key] || key); }
function catLabel(catKey){ return t('cat_'+catKey); }
function localeFor(){ return cfg.lang==='ar'?'ar':(cfg.lang==='zh'?'zh-CN':(cfg.lang==='fr'?'fr':(cfg.lang==='de'?'de':'en-US'))); }
function applyLang(){
  var L = I18N[cfg.lang] || I18N.en;
  document.documentElement.setAttribute('lang', cfg.lang);
  document.documentElement.setAttribute('dir', L.dir);
  document.querySelectorAll('[data-i18n]').forEach(function(el){ el.textContent = t(el.getAttribute('data-i18n')); });
  document.querySelectorAll('[data-i18n-ph]').forEach(function(el){ el.setAttribute('placeholder', t(el.getAttribute('data-i18n-ph'))); });
}
